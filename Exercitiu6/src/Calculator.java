public class Calculator {
    //Acesta este un constructor default , il folosim ca sa instantime
    public Calculator(){

    }
    public int adunbare(int a , int b){
        return a+b;
    }
    public int scadere(int a , int b){
        return a-b;
    }
    public int inmultire(int a , int b){
        return a*b;

    }
    public int impartire(int a , int b){
        return a/b;
    }
}
